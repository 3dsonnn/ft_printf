#include "../libft.h"

int	ft_strchr_count(char *str, int c)
{
	int	res;

	res = 0;
	if (!str || c == '\0')
		return (res);
	while (*str)
	{
		if (*str == c)
			res++;
		str++;
	}
	return (res);
}
